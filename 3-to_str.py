#!/usr/bin/env python3
"""
This module contains a type-annotated function that converts a float to a string.
"""


def to_str(n: float) -> str:
    """
    Converts a floating-point number to its string representation.

    Args:
        n (float): The floating-point number to convert.

    Returns:
        str: The string representation of the float.
    """
    return str(n)
